#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		i;
	int		cont;

	if (!s1 || !s2)
		return (0);
	str = (char *)malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!str)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	cont = 0;
	while (s2[cont] != '\0')
	{
		str[i] = s2[cont];
		cont++;
		i++;
	}
	str[i] = '\0';
	return (str);
}
